package entity;

public class City {
    String ad;
    int ehali;
    String refahSeviyesi;
    String temizlik;
    String tehsil;
    String tibb;
    int limit;

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public int getEhali() {
        return ehali;
    }

    public void setEhali(int ehali) {
        this.ehali = ehali;
    }

    public String getRefahSeviyesi() {
        return refahSeviyesi;
    }

    public void setRefahSeviyesi(String refahSeviyesi) {
        this.refahSeviyesi = refahSeviyesi;
    }

    public String getTemizlik() {
        return temizlik;
    }

    public void setTemizlik(String temizlik) {
        this.temizlik = temizlik;
    }

    public String getTehsil() {
        return tehsil;
    }

    public void setTehsil(String tehsil) {
        this.tehsil = tehsil;
    }

    public String getTibb() {
        return tibb;
    }

    public void setTibb(String tibb) {
        this.tibb = tibb;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }


}
